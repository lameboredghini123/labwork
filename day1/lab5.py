#WAP to calculate & print the area of a circle
#area of circle=pi*r*r

#the value of pi has already been predefined as 3.14, so we store the value in the variable itself
pi=3.14

#asking the user to input the radius value and storing it in the variable 'radius'
radius=int(input("enter the radius value of the circle"))

#calculating the area and storing the value in the variable 'area'
area=pi*radius*radius

#printing the area
print("the area of the circle is",area,"cm\u00b2")