#WAP to calculate & print the volume of a cube
#volume of cube=length*length*length

#asking the user to input the length value of the cube and storing the value in the 'l' variable
l=int(input("enter the length of the cube in cm"))

#calculating the volume and storing its value in the variable vol_ume
vol_ume=l*l*l

#printing the volume
print("the volume of the cube is",vol_ume,"cm\u00b3")