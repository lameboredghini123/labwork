#WAP to calculate & print the sum of two integers

#asking the user to input two integers and storing them in variables 'num1' and 'num2'
num1=int(input("enter a number"))
num2=int(input("enter another number"))

#calculating the sum of both the integers and storing it in another variable 'sum'
sum=num1+num2

#printing the sum stored in the variable
print("the sum of",num1,"and",num2,"is",sum)
